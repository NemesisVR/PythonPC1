# Definir la lista 
lista_original = [1, 1, 2, 3, 4, 4, 5, 1]

# Crear el conjunto a partir de la lista definida
conjunto_sin_duplicados = set(lista_original)

# Convertir el conjunto a lista otra vez
lista_sin_duplicados = list(conjunto_sin_duplicados)

# Imprimir la lista sin duplicados
print("Lista procesada:", lista_sin_duplicados)
